from __future__ import annotations

import json

from .categories import build_schema_description, default_schema


SYSTEM_PROMPT = """你是中国画赏析文本的结构化事实抽取器。

你只能根据输入的“问题”和“赏析文本”工作，禁止补充常识、禁止推测、禁止改写原意。
你的任务是先识别文本里出现的精确要点，再判断这些要点与问题的相关性，并严格匹配给定 JSON 模板。

强制规则：
1. 只抽取事实性、要素性内容，不抽取纯主观赞美、审美判断或泛化评价。
2. 所有要点都必须是精确短词、短语或可核对的事实片段，尽量贴近原文表达。
3. 对 `要素列表` 字段，必须做原子化拆分：每个要素都应是不可再分的最小语义单元，绝不能保留长句。
4. 如果文本没有明确提到某个层级，就标记为“不相关”，并输出空列表 `[]`，不能脑补。
5. 输出必须严格匹配模板的字段名、层级结构和字段类型。
6. 输出只能是合法 JSON，不要出现 markdown 代码块，不要添加解释文字。

特别注意：
- “意境营造”“象征寓意”“艺术地位” 只有在原文明确写出时才能抽取，不能从语气和常识推断。
- 如原文只说“优美”“吸引人”“生动”“富丽”等纯主观形容，而没有明确事实支撑，不要当作关键词。
- 不要把“富丽”“清雅”“生动”“典范”“创新性”“艺术价值”“文化意义”“哲理意蕴”“美学追求”等抽象评价词直接当成关键词。
- 在视觉层和语义层抽取时，优先保留中国画专业术语，例如皴法、描法、墨法、设色体系、三远法、意境词等。
- `强相关` 只用于问题直接询问的核心要素。
- `弱相关` 只用于对回答问题确有帮助的延伸信息，不能因为分类很多就把大多数类都标成弱相关。
- 如果问题聚焦技法、设色、构图、作者、题跋等单一维度，其他无法直接帮助回答的问题维度通常应判为“不相关”。
- 相关性依据“问题内容”判断，而不是依据分类是否常见。"""


def build_user_prompt(question: str, appreciation_text: str, schema_profile: str = "legacy") -> str:
    category_text = build_schema_description(schema_profile)
    schema_text = json.dumps(default_schema(schema_profile), ensure_ascii=False, indent=2)

    if schema_profile == "legacy":
        return f"""请按以下步骤在内部执行，但最终只输出 JSON：

步骤 1：从赏析文本中抽取每个分类下的精确要点（关键词）。
步骤 2：结合问题，判断每个分类是“强相关 / 弱相关 / 不相关”。
步骤 3：如果相关，必须引用赏析文本中的原句；如果相关但文本未涉及，写“未涉及”；如果不相关，写“不相关”。

额外过滤要求：
- 删除纯审美、纯褒义、纯感叹类表达。
- 删除无法直接回答问题的泛化延伸。
- 如果一个条目同时包含事实和评价，只保留其中可核对的事实部分。

问题：
{question}

分类定义：
{category_text}

输出模板：
{schema_text}

赏析文本：
{appreciation_text}
"""

    if schema_profile == "simple_v1":
        return f"""请直接抽取以下 11 个字段的事实信息，最终只输出 JSON：

字段：
- 画名
- 作者
- 朝代
- 技法
- 构图
- 题材
- 形制
- 材质
- 设色方式
- 题跋
- 印章

输出规则：
- 每个字段的值都必须是数组 `[]`。
- 删除 `相关性` 字段，不要输出 `强相关 / 弱相关 / 不相关`。
- 只保留事实性短语，不要输出评论句、审美判断或推测。
- 如果文本没有明确提到该字段，就输出空数组 `[]`。
- `画名 / 作者 / 朝代` 优先保留最明确、最核心的 1 项。
- `技法 / 构图 / 题材 / 形制 / 材质 / 设色方式 / 题跋 / 印章` 允许提取多个原子词，组成数组。
- 上述 8 个字段如果文本里明确出现多个词，例如多个技法、多个构图词、多个印章词，就都保留到 list 里。
- `技法` 和 `构图` 必须尽量抽成最短术语，例如 `披麻皴`、`白描`、`全景式布局`、`留白`。
- `设色方式` 优先提取明确的设色体系词，如 `青绿`、`水墨`、`浅绛`、`重彩`。
- 同一个字段内不要重复，能拆成多个词时尽量拆开，不保留长句。
- 不要因为常识补全作者、朝代、材质、形制，也不要从画名反推信息。
- 不要输出 `未涉及`、`不相关` 之类占位词；没提到就直接给空数组。

问题：
{question}

字段定义：
{category_text}

输出模板：
{schema_text}

赏析文本：
{appreciation_text}
"""

    if schema_profile == "academic_v2":
        return f"""请按以下步骤在内部执行，但最终只输出 JSON：

步骤 1：按层级 schema 对赏析文本做事实性要素抽取。
步骤 2：对每个叶子节点判断“强相关 / 弱相关 / 不相关”。
步骤 3：把可核对的事实短语写入 `要素列表`。

字段规则：
- 每个叶子节点都必须是一个对象，且只包含 `相关性` 与 `要素列表` 两个字段。
- `要素列表` 只允许写事实性内容，不写完整评论句，不写主观赞美，不写推测。
- `要素列表` 中的每一项都必须是不可再分的最小语义单元。
- 例如：`使用了淡墨披麻皴描绘远山` 必须拆成 `["淡墨", "披麻皴", "远山"]`，不能保留整句。
- 如果文本没有明确提到该叶子节点对应的事实，则 `相关性` 必须写 `不相关`，且 `要素列表` 必须是空列表 `[]`。
- 只有在文本明确提到、且与问题有关时，才允许标记为 `强相关` 或 `弱相关`。
- 作品名称、尺寸规格、收藏地点只有在赏析文本中明确提到时才能写入。
- “气韵生动”“意境”“艺术史地位”等只有在文本明确写出时才能写入，不能凭常识补全。
- 在视觉层与语义层，优先保留传统专业术语，如：斧劈皴、披麻皴、白描、破墨、积墨、浅绛、青绿、高远法、平远法、深远法等。

额外过滤要求：
- 删除纯审美、纯褒义、纯感叹类表达。
- 删除无法直接回答问题的泛化延伸。
- 如果一个条目同时包含事实和评价，只保留其中可核对的事实部分。
- 不要把一个长句整体塞进 `要素列表`；必须先拆成最小语义单元后再输出。
- 保持忠于原文，禁止把原文没有写到的上位概念、下位概念或常识背景补进 `要素列表`。

问题：
{question}

分类定义：
{category_text}

输出模板：
{schema_text}

赏析文本：
{appreciation_text}
"""

    raise ValueError(f"Unsupported schema profile: {schema_profile}")
